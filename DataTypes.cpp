#include <iostream>

#include <string>

using namespace std;

int main ()
 {
  // declare Integer variable
  int number = 15;               // Integer (whole number)

  // declare Float type variable
  float floatNmber = 13.56;     // Floating point number

  // declare Float type variable
  double myDoubleNumber = 20.58;   // Floating point number

  // declare Character type variabl
  char letter = 'A';         // Character

  // declare Boolean type variabl
  bool boolean = false;       // Boolean
  // declare String type variabl
  string name = "saddam";   // String

  // Print variable values
  cout << "int: " << number << "\n";
  cout << "float: " << floatNmber << "\n";
  cout << "double: " << myDoubleNumber << "\n";
  cout << "char: " << letter << "\n";
  cout << "bool: " << boolean << "\n";
  cout << "string: " << name << "\n";

  return 0;
}

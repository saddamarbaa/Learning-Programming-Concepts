#include <iostream>
using namespace std;

// the  Driver Code
int main()
{
    for(int i = 20; i >= 2; i--)
    {
        // print numbers from 20 to 2
        cout << i << "\n";
    }
    // add end line after loop
    cout << endl;
     return 0;// signal to operating system everything works fine

}/** End of main function */
